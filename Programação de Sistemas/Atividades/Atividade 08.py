def modifica_numero(x):
  x = x + 10
  print(f"Dentro da função: {x}")

def modifica_lista(lista):
  lista.append(100)
  print(f"Dentro da função: {lista}")

# o começo do programa é na váriavel número
numero = 5

# o começo da segunda parte do programa é na lista
lista = [1, 2, 3]

print(f"Antes de chamar a função: {numero}")
# logo após ela acessa a função modifica_numero e nessa função
# ele irá executar a soma x = 5 + 10 = 15
modifica_numero(numero)
# aqui o número chegará modificado, com o novo resultado
print(f"Depois de chamar a função: {numero}")

print(f"Antes de chamar a função: {lista}")
# logo após a lista acessa a função modifica_lista e nessa função
# ele irá adicionar o número 100 no final da lista
modifica_lista(lista)
# aqui irá mostrar o conteúdo da nova lista
print(f"Depois de chamar a função: {lista}")

