def multiplica(a,b):
  return a * b

def soma(a,b):
  return a + b

def calcula():
  x = multiplica(2,3)
  y = soma(x,4)
  return y

def main():
  resultado = calcula()
  print("Resultado final: ", resultado)

main()

"""A execução começa com a chamada da função main().

1) main() chama calcula(): Dentro de main(), a linha resultado = calcula() inicia
uma chamada para a função calcula().

2) calcula() chama multiplica(): Dentro de calcula(), a linha x = multiplica(2,3) inicia uma chamada para a função multiplica() com os argumentos 2 e 3.

3) calcula() chama soma(): Após multiplica() retornar, a linha y = soma(x,4) inicia uma chamada para a função soma() com os argumentos x (que é o resultado de multiplica(2,3), ou seja, 6) e 4.

4) main() chama print(): Após calcula() retornar, a função main() continua e chama a função embutida print() para exibir o resultado.

"""

